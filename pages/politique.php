<div class="politique">
    <p>La présente politique de protection des données personnelles peut être modifiée ou complétée à tout moment par notre société, notamment en vue de se conformer à toute évolution légale, réglementaire, jurisprudentielle ou technologique. Dans un tel cas, la date de sa mise à jour sera clairement identifiée en tête de la présente politique. Ces modifications engagent l’Utilisateur dès leur mise en ligne. Il convient par conséquent que l’Utilisateur consulte régulièrement la présente politique de protection des données personnelles et d’utilisation des cookies afin de prendre connaissance de ses éventuelles modifications.</p>

    <h3>1. Collecte de vos données personnelles</h3>
    <p>Nous collectons et traitons notamment vos nom, prénom, adresse e-mail, date de naissance, mot de passe, historiques de commandes.</p>
    <p>Les données personnelles qui sont collectées sur notre site internet sont principalement utilisées par l’éditeur pour la gestion des relations avec vous, et le cas échéant pour le traitement de vos commandes.</p>

    <h3>2. Destinataires de vos données personnelles</h3>
    <p>Les données personnelles collectées sur notre site sont exclusivement destinées à notre société. Nous ne les transmettons à aucun tiers.</p>

    <h3>3. Durée de conservation de vos données personnelles</h3>

    <p>Nous conservons vos données personnelles pour la durée nécessaire à l’exécution des prestations de services ou fourniture de biens souscrits par vous auprès de notre société, ainsi que pour satisfaire à nos obligations légales.</p>

    <h3>4. Sécurité de vos données personnelles</h3>
    <p>Nous vous informons prendre toutes précautions utiles, mesures organisationnelles et techniques appropriées pour préserver la sécurité, l’intégrité et la confidentialité de vos données personnelles et notamment, empêcher qu’elles soient déformées, endommagées ou que des tiers non autorisés y aient accès.</p>

    <h3>5. Cookies</h3>
    <p>Notre site internet utilise des cookies. Il s’agit de petits fichiers texte enregistrés sur votre disque dur. Les cookies utilisés sur notre site nous permettent de vous proposer les services suivants : mémorisation des articles de votre panier d’achat, identification lors de la connexion à votre compte client, mémorisation de vos consultations, personnalisation des offres que nous vous proposons en fonction de votre historique de navigation.</p>

